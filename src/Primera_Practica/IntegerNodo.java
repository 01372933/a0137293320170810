public class IntegerNodo {
    public int value;
    public IntegerNodo next;
    
    public IntegerNodo(){
        this.value=0;
        this.next=null;
    }
    
    public IntegerNodo(int value){
        this.value=value;
        this.next=null;
    }
    
}
